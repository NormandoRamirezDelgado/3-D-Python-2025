# Hacer un programa que calcule el 10% de una venta 
# realizada e imprimir valor original, el porcentaje obtenido y 
# el total descontando el porcentaje.
venta = 1000.00
porcentaje = venta * 0.10
total = venta - porcentaje
print('La venta $', venta)
print('El Porcentaje $', porcentaje)
print('Total $', total)




